# Mapping school codes to location pools (each school has its own 30+ list)
SCHOOL_LOCATION_MAP = {
    "sma_negeri_1_majenang": list(LOCATIONS_MAJENANG_CILACAP),
    "smk_komputama_majenang": list(LOCATIONS_MAJENANG_CILACAP),
    "sma_negeri_2_cilacap": list(LOCATIONS_CILACAP_KOTA),
    "sma_negeri_1_purwokerto": list(LOCATIONS_PURWOKERTO_BANYUMAS),
    "sma_negeri_4_purwokerto": list(LOCATIONS_PURWOKERTO_BANYUMAS),
    "sma_negeri_1_bantarkawung": list(LOCATIONS_BANTARKAWUNG_BREBES),
}


def get_locations_for_school(school_code: str) -> list:
    """
    Get location pool for a specific school code.
    Returns list of location dicts with kelurahan, kecamatan, kabupaten, provinsi
    """
    locations = SCHOOL_LOCATION_MAP.get(school_code)
    if not locations:
        # Fallback to Majenang if school code not found
        return LOCATIONS_MAJENANG_CILACAP
    return locations


def get_location_count_for_school(school_code: str) -> int:
    """Get number of available locations for a school"""
    return len(get_locations_for_school(school_code))
